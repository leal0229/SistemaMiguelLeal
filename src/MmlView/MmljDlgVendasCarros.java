    private void mml_jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mml_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        MmlVendasCarros vendaCarro = new MmlVendasCarros();
        int codigo = Integer.parseInt(mml_jTxtIdVendasCarros.getText());
        vendaCarro.setIdVendasCarros(codigo);
        vendaCarro.setIdVenda(mml_jTxtIdVendas.getText());
        vendaCarro.setIdCarros(mml_jTxtIdCarros.getText());
        vendaCarro.setQuantidade(Util.strToInt(mml_jTxtQuantidade.getText()));
        vendaCarro.setValorUnitario(Util.strToDouble(mml_jTxtValorUnitario.getText()));

        MmlVendasCarrosDao vendasCarrosDao = new MmlVendasCarrosDao();
        vendasCarrosDao.insert(vendaCarro);
        habilitar(false);

    }//GEN-LAST:event_mml_jBtnConfirmarActionPerformed

